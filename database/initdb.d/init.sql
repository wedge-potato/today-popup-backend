	`start_time` TIME NULL COMMENT '시작 시간',
	`end_time` TIME NULL COMMENT '종료 시간',
	`create_date` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	`update_date` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);


